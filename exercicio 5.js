// Crie uma função que receba dois números e retorne se o primeiro é maior ou igual ao segundo.

function ehMaiorOuIgual(num1, num2) {
    return num1 >= num2;
  }
  
  // Exemplos de uso
  console.log(ehMaiorOuIgual(10, 5)); // true
  console.log(ehMaiorOuIgual(5, 10)); // false
  console.log(ehMaiorOuIgual(10, 10)); // true
  